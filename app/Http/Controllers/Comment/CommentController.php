    public function eye($id)
    {

        $affected = Comment::find($id);
        if ($affected->is_active == 1) {
            $affected->is_active = 0;
            $affected->save();
            echo ' <i style="color: red;" class="fa fa-eye-slash"></i>';
        } else {
            $affected->is_active = 1;
            $affected->save();
            echo '<i  class="fa fa-eye"></i>';
        }


